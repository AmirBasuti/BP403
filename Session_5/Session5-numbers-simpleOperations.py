# float numbers
print(4.2)
print(type(4.2))

print(5.0)
print(type(5.0))

# complex numbers
print(8+3j) # 8 + 3j, j equals (-1)**0.5
print(type(8+3j))

print(2+6j)
print(type(2+6j))

